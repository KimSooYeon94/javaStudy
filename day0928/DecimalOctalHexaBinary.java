class DecimalOctalHexaBinary 
{
	public static void main(String[] args) 
	{
		int a = 12;  //10진수
		int b = 014;  //8진수
		int c = 0xC;  //16진수
		int d = 0b1100;  //2진수

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);

	}
}
